#!/usr/bin/env python3
"""Generate stats and summary for the iterate site.

Generates:
1. stats.json — live stats for the site (commits, lines, tests, coverage)
2. memory/weekly_summary.md — weekly summary of what happened
"""

import json
import subprocess
import sys
from datetime import datetime, timedelta


def run(cmd, cwd="."):
    """Run a command and return output."""
    try:
        result = subprocess.run(
            cmd, shell=True, cwd=cwd, capture_output=True, text=True, timeout=30
        )
        return result.stdout.strip()
    except Exception:
        return ""


def get_commit_count(repo_path="."):
    """Count total commits."""
    return run("git rev-list --count HEAD", repo_path)


def get_commits_this_week(repo_path="."):
    """Count commits in the last 7 days."""
    week_ago = (datetime.now() - timedelta(days=7)).strftime("%Y-%m-%d")
    return run(f"git rev-list --count --since='{week_ago}' HEAD", repo_path)


def get_lines_changed_this_week(repo_path="."):
    """Count lines added/removed this week."""
    week_ago = (datetime.now() - timedelta(days=7)).strftime("%Y-%m-%d")
    diff = run(
        f"git log --since='{week_ago}' --pretty=tformat: --numstat | awk '{{a+=$1;b+=$2}} END {{print a, b}}'",
        repo_path,
    )
    parts = diff.split()
    if len(parts) == 2:
        return {"added": int(parts[0]), "removed": int(parts[1])}
    return {"added": 0, "removed": 0}


def get_contributors(repo_path="."):
    """Count unique contributors."""
    out = run("git shortlog -sne HEAD", repo_path)
    return len([l for l in out.split("\n") if l.strip()])


def get_languages(repo_path="."):
    """Get language breakdown."""
    langs = {}
    out = run(
        "find . -name '*.go' -not -path './vendor/*' | xargs wc -l 2>/dev/null | tail -1",
        repo_path,
    )
    if out:
        parts = out.split()
        if parts:
            langs["Go"] = int(parts[0])
    return langs


def get_test_count(repo_path="."):
    """Count test functions."""
    out = run("grep -rn 'func Test' --include='*_test.go' . | wc -l", repo_path)
    try:
        return int(out)
    except ValueError:
        return 0


def get_journal_entries(repo_path="."):
    """Count journal entries."""
    out = run("grep -c '^## Day' docs/JOURNAL.md", repo_path)
    try:
        return int(out)
    except ValueError:
        return 0


def get_recent_journal(repo_path="."):
    """Get recent journal entries."""
    out = run("grep -A3 '^## Day' docs/JOURNAL.md | head -20", repo_path)
    return out


def generate_stats(repo_path="."):
    """Generate stats.json for the site."""
    stats = {
        "generated_at": datetime.utcnow().strftime("%Y-%m-%dT%H:%M:%SZ"),
        "total_commits": get_commit_count(repo_path),
        "commits_this_week": get_commits_this_week(repo_path),
        "lines_changed": get_lines_changed_this_week(repo_path),
        "contributors": get_contributors(repo_path),
        "languages": get_languages(repo_path),
        "test_count": get_test_count(repo_path),
        "journal_entries": get_journal_entries(repo_path),
    }
    return stats


def generate_weekly_summary(repo_path="."):
    """Generate weekly summary markdown."""
    stats = generate_stats(repo_path)
    recent = get_recent_journal(repo_path)

    summary = f"""## Weekly Summary — {datetime.now().strftime("%Y-%m-%d")}

### Stats
- **Commits this week:** {stats["commits_this_week"]}
- **Lines added:** {stats["lines_changed"]["added"]}
- **Lines removed:** {stats["lines_changed"]["removed"]}
- **Test count:** {stats["test_count"]}
- **Journal entries:** {stats["journal_entries"]}

### Recent Activity
{recent}

---
*Auto-generated by iterate*
"""
    return summary


def main():
    repo_path = sys.argv[1] if len(sys.argv) > 1 else "."

    # Generate stats
    stats = generate_stats(repo_path)
    stats_file = f"{repo_path}/docs/stats.json"
    with open(stats_file, "w") as f:
        json.dump(stats, f, indent=2)
    print(f"Stats written to {stats_file}")
    print(json.dumps(stats, indent=2))

    # Generate weekly summary
    summary = generate_weekly_summary(repo_path)
    summary_file = f"{repo_path}/memory/weekly_summary.md"
    with open(summary_file, "w") as f:
        f.write(summary)
    print(f"\nSummary written to {summary_file}")


if __name__ == "__main__":
    main()
